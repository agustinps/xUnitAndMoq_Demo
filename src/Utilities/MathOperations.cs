namespace Utilities
{
    public static class MathOperations
    {
        /// <summary>
        /// Método para sumar dos números para el ejemplo de tests con xUnit
        /// Esta clase sirve para las pruebas en el proyecto xUnit TheoryDemoTest
        /// </summary>
        /// <param name="a"> valor a sumar </param>
        /// <param name="b"> valor a sumar </param>
        /// <returns></returns>
        public static int Add(int a, int b)
        {
            return a + b;
        }
    }
}
